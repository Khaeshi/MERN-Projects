'use client'

import React, { useEffect } from 'react';
import { useAuth } from '../../context/AuthContext';
import { usePathname } from 'next/navigation';

const API_URL = process.env.NEXT_PUBLIC_API_URL || 'http://localhost:5000';

export function LoginModal() {
  const { showLoginModal, setShowLoginModal, user, loading } = useAuth();
  const pathname = usePathname();

  // Don't show modal on admin pages or auth pages
  const isAdminPage = pathname?.startsWith('/admin');
  const isAuthPage = pathname?.startsWith('/auth');

  const handleGoogleLogin = () => {
    if (typeof window !== 'undefined') {
      localStorage.setItem('redirectAfterLogin', window.location.pathname);
    }
    
    window.location.href = `${API_URL}/api/auth/google?prompt=select_account`;
  };

  // Auto-show modal for customer pages when not logged in
  useEffect(() => {
    if (user || isAdminPage || isAuthPage || loading) {
      setShowLoginModal(false);
      return;
    }

    // Show modal after a short delay
    const timer = setTimeout(() => {
      setShowLoginModal(true);
    }, 500);
    
    return () => clearTimeout(timer);
  }, [user, isAdminPage, isAuthPage, loading, showLoginModal, setShowLoginModal]);

  // Don't render on admin/auth pages or if user is logged in
  if (isAdminPage || isAuthPage || !showLoginModal || user) return null;

  return (
    <div className="fixed inset-0 bg-black/50 flex items-center justify-center z-50 backdrop-blur-sm">
      <div className="bg-stone-800 rounded-xl p-8 max-w-md w-full mx-4 shadow-2xl animate-slide-up border border-stone-700">
        <div className="mb-6">
          <h2 className="text-2xl font-bold text-white">Welcome to Cafe Prince</h2>
          <p className="text-sm text-stone-400 mt-1">Sign in to browse menu and order</p>
        </div>

        <p className="text-stone-300 mb-6">
          Sign in with your Google account to browse our menu and place orders.
        </p>

        <button
          onClick={handleGoogleLogin}
          className="w-full flex items-center justify-center gap-3 bg-white hover:bg-gray-50 text-gray-700 font-medium py-3 px-6 rounded-lg transition-all duration-200 shadow-sm hover:shadow-md"
        >
          <svg className="w-6 h-6" viewBox="0 0 24 24">
            <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
            <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
            <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
            <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
          </svg>
          Sign in with Google
        </button>

        <div className="mt-6 pt-6 border-t border-stone-700">
          <p className="text-sm text-stone-400 text-center">
            Staff member?{' '}
            <a href="/login" className="text-amber-400 hover:text-amber-300 font-medium">
              Login here
            </a>
          </p>
        </div>

        <p className="text-xs text-stone-500 text-center mt-4">
          By signing in, you agree to our Terms of Service and Privacy Policy.
        </p>
      </div>
    </div>
  );
}